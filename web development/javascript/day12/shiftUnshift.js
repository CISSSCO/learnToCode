//shift
//removes a value from the start of the array

let a = ["cisco", "ramon", "ram", "raj", "abhi"];

let removedVal = a.shift();
console.log(a);
console.log(removedVal);

//unshift
//adds a value at the start of the array

let returnAfterUnshift = a.unshift("Cisco Ramon");
console.log(a);
console.log(returnAfterUnshift);